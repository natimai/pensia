// פונקציה לערבב שאלות
function shuffleQuestions(questions) {
    const shuffled = [...questions];
    for (let i = shuffled.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    return shuffled;
}

// פונקציה לערבב תשובות
function shuffleAnswers(question) {
    const shuffledAnswers = [...question.answers];
    const correctAnswerText = question.answers[question.correct];
    
    // ערבוב מערך התשובות
    for (let i = shuffledAnswers.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [shuffledAnswers[i], shuffledAnswers[j]] = [shuffledAnswers[j], shuffledAnswers[i]];
    }
    
    // מציאת המיקום החדש של התשובה הנכונה
    const newCorrectIndex = shuffledAnswers.indexOf(correctAnswerText);
    
    return {
        ...question,
        answers: shuffledAnswers,
        correct: newCorrectIndex
    };
}

// פונקציה לקבלת שאלות רנדומליות למשחק
function getRandomQuestions(count = 10) {
    const shuffledQuestions = shuffleQuestions(questions);
    const selectedQuestions = shuffledQuestions.slice(0, count);
    
    // ערבוב תשובות לכל שאלה
    return selectedQuestions.map(question => shuffleAnswers(question));
} 